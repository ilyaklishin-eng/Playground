export function normalizeLanguageCode(value = "") {
  const language = String(value || "").trim().toLowerCase();
  if (language.includes("english")) return "en";
  if (language.includes("french")) return "fr";
  if (language.includes("german") || language.includes("deutsch")) return "de";
  if (language.includes("spanish")) return "es";
  return "ru";
}

function normalizeFormatTokens(value = "") {
  const raw = String(value || "").toLowerCase();
  const tokens = new Set();
  if (/(text|feature|текст)/.test(raw)) tokens.add("text");
  if (/(video|видео)/.test(raw)) tokens.add("video");
  if (/(podcast|подкаст)/.test(raw)) tokens.add("podcasts");
  return tokens;
}

export function matchesFormatFilter(item, filterValue = "all") {
  if (filterValue === "all") return true;
  const tokens = normalizeFormatTokens(item.format);
  return tokens.has(filterValue);
}

export function localizeInterviewItem(item, uiLang = "en") {
  const locale = ["en", "fr", "de", "es"].includes(uiLang) ? uiLang : "en";
  const byUrl = CONTENT_BY_URL[String(item.url || "").trim()] || {};
  const localized = byUrl[locale] || byUrl.en || null;
  const languageCode = normalizeLanguageCode(item.language);
  const languageLabel =
    (LANGUAGE_LABELS[locale] && LANGUAGE_LABELS[locale][languageCode]) ||
    (LANGUAGE_LABELS.en && LANGUAGE_LABELS.en[languageCode]) ||
    item.language;
  const formatLabel =
    (FORMAT_LABELS[String(item.format || "").trim()] &&
      FORMAT_LABELS[String(item.format || "").trim()][locale]) ||
    item.format;

  return {
    ...item,
    languageCode,
    languageLabel,
    formatLabel,
    title: localized?.title || item.title,
    description: localized?.description || item.description,
  };
}
